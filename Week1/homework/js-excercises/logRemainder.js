'use strict'
/*
1. x=1, if x equals 7 then x%3 means the remainder of dividing 7 by 3. 7 divided by 3 equals 2 with a remainder of 1 (7-(2*3)=1).*/
let x=7;
x=x%3;
console.log(x);
/*
2. y=1, if y equals 21 then 21%4 means the remainder of dividing 21 by 4. 21 divided by 4 equals 5 with a remainder of 1 (21-(5*4)=1).*/
let y=21;
y=y%4;
console.log(y);
/*
3. Similarly z=1. 13/2=6 with a remainder of 1 (13-(6*2)=1).*/
let z=13;
z=z%2;
console.log(z);

