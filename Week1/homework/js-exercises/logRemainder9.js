"use strict";

// Initialize x, y, z
let x = 7;
let y = 21;
let z = 13;

// The remainder / modulus operator (%) returns the remainder after (integer) division.
x = x % 3; // 7 - (3 * 2) = 1
y = y % 4; // 21 - (4 * 5) = 1
z = z % 2  // 13 - (2 * 6) = 1

console.log(x);
console.log(y);
console.log(z);
