'use strict';
/* bang operator work by return the reminding after value divided.
 Ex,if we divide 7 by 3 it will equal 2 and return of the 7 after has been divided 1 */
let x = 7; //declare variable
x %= 3; //same as z=z %2
console.log('X reminder is ' + x);

let y = 21; //declare variable
y = y % 4; //short hand  y %=4;
console.log(`Y reminder is ${y}`);

let z = 13; //declare variable
z %= 2; //same as z=z % 2;
console.log('Z reminder is ' + z);
