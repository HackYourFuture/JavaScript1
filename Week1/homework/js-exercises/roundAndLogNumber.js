"use strict";

//Declare floating point number
let z = 7.25;

//Log the number before round it
console.log(z);

//Round the floating point number to nearest integer
let a = Math.round(z);

//Log the number after round it
console.log(a);

//Compare two numbers who is the bigger
let maxNumber = Math.max(a, z);
console.log(maxNumber);